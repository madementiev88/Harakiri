/** Get service duration in minutes by service object or ID */
export function getServiceDuration(s: any): number {
  const id = typeof s === 'number' ? s : s.id;
  if (SERVICE_DURATION[id]) return SERVICE_DURATION[id];
  if (s.seance_length) return Math.round(s.seance_length / 60);
  return 60;
}
